#include "fillit.h"

void			free_twod(char **str, int i)
{
	int		j;

	j = 0;
	if (str == NULL || *str == NULL || i == 0)
		return ;
	while (str[j])
	{
		free(str[j]);
		j++;
	}
	free(str);
}
