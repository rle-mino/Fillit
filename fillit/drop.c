#include "fillit.h"

int			drop(char *result, const char *str)
{
	int 	i;
	int		j;

	j = 0;
	i = 0;
	while (i < 21)
	{
		while ((str[i] == '.' || str[i] == '\n') && str[i])
			i++;
		while (str[i] >= 'A' && str[i] <= 'Z' && str[i])
		{
			result[i] = str[i];
			i++;
		}
	}
	return (0);
}
