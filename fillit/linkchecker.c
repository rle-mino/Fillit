#include "fillit.h"

int		linkchecker(char *str, int i, int nb)
{
	if (nb < 4)
	{
		if (str[i + 1] != '#' && str[i + 5] != '#')
		{
			while (str[i - 1] == '#')
			{
				if (str[i + 4] == '#')
					return (1);
				i--;
			}
			return (0);
		}
	}
//	else if (nb > 4)
//		return (0);
	return (1);
}
